package com.innohotsource.hotjson.Json.sample;

public class SubSample {
    private String subName;
    private Long subId;
    private Sub sub;
    public SubSample(){};

    @Override
    public String toString() {
        return "SubSample{" +
                "subName='" + subName + '\'' +
                ", subId=" + subId +
                ", sub=" + sub +
                '}';
    }
}
